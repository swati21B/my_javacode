package java_Code;

public class Loops_nested_If {
	public static void main (String[] args) {
	int age = 17;
	
	if(age<18)//false condition will not execute
	{
		System.out.println("false");
	}
	if(age>=25)
	{
		System.out.println("false");
	}
	if(age<20)
	{
		System.out.println("true");
	}
	if(age!=20)
	{
		System.out.println("true");
	}
		
	}

}
