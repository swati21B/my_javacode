package java_Code;

public class SwitchCaseBreakDefault {

	public static void main(String[] args) {
		switch(6)
		{
		case 1: 
			System.out.println("Launch Google chrome");
			break;
		case 2:
			System.out.println("Launch Mozilla Firefox");
			break;
		case 3:
		System.out.println("Launch Opera");	
			break;
		case 4:
			System.out.println("Launch Microsoft Edge");
			break;
			
		default:
		System.out.println("Sorry your selection is wrong");
		}

	}

}
