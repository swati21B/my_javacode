package newpackage;

public class BasicJavaOperators {
	
public static void main(String[] args) {
	byte pencil = 50;
	short pouch = 20000;
	int sum = pencil + pouch;
	System.out.println(sum);
	
	int fourdigit = 2500;
	float decivalue = 20.55f;
	float sub = fourdigit - decivalue;
	System.out.println(sub);
	
	double kidweight = 25.65879;
	float adultweight = 70.56f;
	double multi = kidweight * adultweight;
	System.out.println(multi);
	
	int num1 = 100;
	int num2 = 25;
	int div = num1/num2;
	System.out.println(div);
	
	byte modoper = 25%4;
	System.out.println(modoper);
	
}

}
